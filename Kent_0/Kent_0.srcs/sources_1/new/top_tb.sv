//==============================================
// timescale
//==============================================
`timescale 1ns / 1ps

//==============================================
// top_tb
//==============================================
module top_tb;

    //==============================
    // dut
    //==============================
    top dut
    (
        .clk(clk),
        .rst(rst)
    );

    //==============================
    // logic
    //==============================
    logic clk;
    logic rst;
    
    //==============================
    // clock
    //==============================
    always begin
        #5 clk = !clk;
    end
    
    //==============================
    // initial
    //==============================
    initial begin
        clk = 0;
        rst = 0;
        
        #10;
        
        rst = 1;
        
        #10;
        
        rst = 0;
        
        #100;

    end
    
endmodule
