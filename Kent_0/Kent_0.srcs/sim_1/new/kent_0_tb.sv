//==============================================
module kent_0_tb;

    //==============================
    // dut
    //==============================
    kent_0 dut
    (
        .clk(clk),
        .ext_rst(ext_rst),
        .port_0_data(port_0_data),
        .port_1_data(port_1_data)
    );

    //==============================
    // logic
    //==============================
    logic clk;
    logic ext_rst;
    logic [7:0] port_0_data;
    logic [7:0] port_1_data;
    
    //==============================
    // clock
    //==============================
    always begin
        #5 clk = !clk;
    end
    
    //==============================
    // initial
    //==============================
    initial begin
        clk = 0;
        ext_rst = 0;
        
        #10;
        
        ext_rst = 1;
        
        #10;
        
        ext_rst = 0;
        
        #100;

    end
    
endmodule
