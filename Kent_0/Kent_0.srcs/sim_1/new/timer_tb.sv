//==============================================
// timescale
//==============================================
`timescale 1ns / 1ps

//==============================================
// timer_tb
//==============================================
module timer_tb;

    //==============================
    // dut
    //==============================
    timer dut
    (
        .clk(clk),
        .rst(rst),
        .N(N),
        .ov(ov)
    );

    //==============================
    // logic
    //==============================
    logic clk;
    logic rst;
    logic [15:0] N;
    logic ov;
    
    //==============================
    // clock
    //==============================
    always begin
        #5 clk = !clk;
    end
    
    //==============================
    // initial
    //==============================
    initial begin
        clk = 0;
        rst = 0;
        N = 16'h0004;
        
        #10;
        
        rst = 1;
        
        #10;
        
        rst = 0;
        
        #200;
        
        N = 16'h0000;
        
        #10;
        
        rst = 0;
        
        #100;
    end
    
    
endmodule