//==============================================
// timescale
//==============================================
`timescale 1ns / 1ps

//==============================================
// clocks_tb
//==============================================
module clocks_tb;

    //==============================
    // dut
    //==============================
    clocks dut
    (
        .clk(clk),
        .rst(rst),
        .clk_div_8(clk_div_8)
    );

    //==============================
    // logic
    //==============================
    logic clk;
    logic rst;
    logic clk_div_8;
    
    //==============================
    // clock
    //==============================
    always begin
        #5 clk = !clk;
    end
    
    //==============================
    // initial
    //==============================
    initial begin
        clk = 0;
        rst = 0;
        
        #10;
        
        rst = 1;
        
        #10;
        
        rst = 0;
        
        #500;


    end
    
    
endmodule